#include "sharedmem.h"

#define SIZE 192

void init_sharedmem()
{
    // create shared mem
    int fd = shm_open("sharedmem", O_CREAT | O_EXCL | O_RDWR,
                                 S_IRUSR | S_IWUSR);
        if (fd == -1)
        {
            printf("shm_open failed\n");
            exit(1);
        }
    // set size of shared mem object              
    if (ftruncate(fd, SIZE) == -1)
    {
        printf("ftruncate failed\n");
        exit(1);
    }
    // map shared mem into memory
    uint64_t* sharedmem = (uint64_t*) mmap(NULL, SIZE, PROT_READ | PROT_WRITE, 
                    MAP_SHARED, fd, 0);
    if (sharedmem == MAP_FAILED)
    {
        printf("mmap failed\n");
        exit(1);
    }
}

void open_sharedmem()
{
    // open existing shared mem
    int fd = shm_open("sharedmem", O_RDWR, 0);
    if (fd == -1)
    {
        printf("shm_open failed\n");
        exit(1);
    }
    // map shared mem into memory
    uint64_t* sharedmem = (uint64_t*) mmap(NULL, SIZE, PROT_READ | PROT_WRITE, 
                    MAP_SHARED, fd, 0);
    if (sharedmem == MAP_FAILED)
    {
        printf("mmap failed\n");
        exit(1);
    }
}

